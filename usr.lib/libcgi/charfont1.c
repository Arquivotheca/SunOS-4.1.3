int             subfont;
char            ch;
char          **pts;
int            *nglyphs;
{
    register int    i;

    i = char_per_font * subfont + (ch - ' ');
    if (i >= 0 && i < sizeof(p) / sizeof(p[0]))
	*pts = p[i];
    else
	*pts = error_glyph;
    *nglyphs = char_per_font;
}
